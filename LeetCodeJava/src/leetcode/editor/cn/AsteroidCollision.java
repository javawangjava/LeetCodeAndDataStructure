        public int[] asteroidCollision(int[] ats) {
            if (ats == null || ats.length == 0) {
                return new int[0];
            }
            Deque<Integer> stack = new LinkedList<>();
            int len = ats.length;
            for (int i = 0; i < len; i++) {
                boolean alive = true;
                while (alive && !stack.isEmpty() && stack.peek() > 0 && ats[i] < 0) {
                    alive = stack.peek() < -ats[i];
                    if (stack.peek() <= -ats[i]) {
                        stack.pop();
                    }
                }
                if (alive) {
                    stack.push(ats[i]);
                }
            }
            int size = stack.size();// æ ˆä¸­å…ƒç´ ä¸ªæ•°
            int[] ans = new int[size];
            for (int i = size - 1; i >= 0; i--) {//éå†å°†æ ˆä¸­å…ƒç´ å¼¹å‡ºï¼Œæ ˆå…ˆè¿›åå‡º
                ans[i] = stack.pop();
            }
            return ans;
        }




